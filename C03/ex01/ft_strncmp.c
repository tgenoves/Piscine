#include <unistd.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	b;

	if (n == 0)
		return (0);
	b = 0;
	while (s1[b] != '\0' && s2[b] != '\0' && s1[b] == s2[b] && b < n)
		b++;
	if (b == n)
		b--;
	return (s1[b] - s2[b]);
}
