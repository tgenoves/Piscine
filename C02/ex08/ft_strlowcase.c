#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	int	e;

	e = 0;
	while (str[e] != '\0')
	{
		if (str[e] > 64 && str[e] < 91)
		{
			(str[e] = str[e] + 32);
		}
		e++;
	}
	return (str);
}
