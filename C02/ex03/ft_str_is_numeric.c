#include <unistd.h>

int	ft_str_is_numeric(char *str)
{
	int	f;

	f = 0;
	while (str[f] != '\0')
	{
		if (str[f] < '0' || str[f] > '9')
		{
			return (0);
		}
		f++;
	}
	return (1);
}
