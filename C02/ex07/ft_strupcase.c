#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int	d;

	d = 0;
	while (str[d] != '\0')
	{
		if (str[d] > 96 && str[d] < 123)
		{
			(str[d] = str[d] - 32);
		}
		d++;
	}
	return (str);
}
