#include <unistd.h>

int	ft_str_is_printable(char *str)
{
	int	b;

	b = 0;
	while (str[b] != '\0')
	{
		if (str[b] < 32 || str[b] > 126)
		{
			return (0);
		}
		b++;
	}
	return (1);
}
