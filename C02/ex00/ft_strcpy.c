#include <unistd.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	f;

	f = 0;
	while (src[f] != '\0')
	{
		dest[f] = src[f];
		f++;
	}
	dest[f] = '\0';
	return (dest);
}
